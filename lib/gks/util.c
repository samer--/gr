gks_list_t *gks_list_find(gks_list_t *list, int element)
{
  while (list != NULL)
    {
      if (list->item == element)
        return list;
      list = list->next;
    }
  return NULL;
}

gks_list_t *gks_list_add(gks_list_t *list, int element, void *ptr)
{
  gks_list_t *head, *prev = NULL;

  head = list;
  while (list != NULL)
    {
      prev = list;
      list = list->next;
    }

  list = (gks_list_t *) gks_malloc(sizeof(gks_list_t));

  list->item = element;
  list->ptr = ptr;
  list->next = NULL;
  if (prev != NULL)
    prev->next = list;
  else
    head = list;

  return head;
}

gks_list_t *gks_list_del(gks_list_t *list, int element)
{
  gks_list_t *head, *prev = NULL, *next;

  head = list;
  while (list != NULL)
    {
      if (list->item == element)
	{
	  next = list->next;
	  gks_free((void *) list);

	  if (prev != NULL)
	    prev->next = next;
	  else
	    head = next;
	  break;
	}
      else
	{
	  prev = list;
	  list = list->next;
	}
    }

  return head;
}

void gks_list_free(gks_list_t *list)
{
  gks_list_t *next;

  while (list != NULL)
    {
      next = list->next;
      if (list->ptr != NULL)
	gks_free(list->ptr);
      gks_free((void *) list);
      list = next;
    }
}

